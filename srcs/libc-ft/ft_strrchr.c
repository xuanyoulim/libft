#include <libft.h>

char		*ft_strrchr(const char *s, int c)
{
	char	*out;
	int		i;

	i = ft_strlen(s);
	out = NULL;
	while (i >= 0)
	{
		if (*s == (char)c)
			out = (char *)s;
		s++;
		i--;
	}
	return (out);
}
