#include <libft.h>

char		*ft_strchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char *)s;
	while (*s)
	{
		if (*s == c)
			return (ptr);
		ptr++;
		s++;
	}
	if (c == '\0')
		return (ptr);
	return (NULL);
}
